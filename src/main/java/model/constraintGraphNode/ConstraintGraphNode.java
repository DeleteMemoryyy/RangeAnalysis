package model.constraintGraphNode;

abstract public class ConstraintGraphNode {
    protected String name;

    public ConstraintGraphNode(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
